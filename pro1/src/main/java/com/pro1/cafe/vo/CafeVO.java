package com.pro1.cafe.vo;

public class CafeVO {

    private long cafeUid;

    private String cafeName;

    private String cafeCategory;

    private String cafeDomTop;

    private String cafeDomMid;

    private String cafeDomBot;

    private String cafeLevel;

    public String getCafeLevel() {
	return cafeLevel;
    }

    public void setCafeLevel(String cafeLevel) {
	this.cafeLevel = cafeLevel;
    }

    public long getCafeUid() {
	return cafeUid;
    }

    public void setCafeUid(long cafeUid) {
	this.cafeUid = cafeUid;
    }

    public String getCafeName() {
	return cafeName;
    }

    public void setCafeName(String cafeName) {
	this.cafeName = cafeName;
    }

    public String getCafeCategory() {
	return cafeCategory;
    }

    public void setCafeCategory(String cafeCategory) {
	this.cafeCategory = cafeCategory;
    }

    public String getCafeDomTop() {
	return cafeDomTop;
    }

    public void setCafeDomTop(String cafeDomTop) {
	this.cafeDomTop = cafeDomTop;
    }

    public String getCafeDomMid() {
	return cafeDomMid;
    }

    public void setCafeDomMid(String cafeDomMid) {
	this.cafeDomMid = cafeDomMid;
    }

    public String getCafeDomBot() {
	return cafeDomBot;
    }

    public void setCafeDomBot(String cafeDomBot) {
	this.cafeDomBot = cafeDomBot;
    }

}
